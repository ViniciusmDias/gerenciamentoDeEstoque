        ProdutoDao dao = new ProdutoDao();
        p.setNome(c_prod_nome.getText());
        p.setQuantidade(Integer.parseInt(c_prod_qtd.getText()));
        
        dao.create(p);

        modoProd = "Navegar";
        ManipulaInterfaceProd();
        limpaTextoProd();
        
        readJTable();
