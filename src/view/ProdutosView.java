        public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tbl_prod_prods3.getModel();
        modelo.setNumRows(0);
        ProdutoDao pdao = new ProdutoDao();
        
        for(Produto p: pdao.read()){
            
            modelo.addRow(new Object[]{
                
                p.getQuantidade(),
                p.getNome()
                
                
            });
            
        }
        
    }
    
    public void ManipulaInterfaceProd(){
        switch(modoProd){
            case "Navegar":
                btn_prod_salvar.setEnabled(false);
                btn_prod_voltar.setEnabled(true);
                c_prod_qtd.setEnabled(false);
                c_prod_nome.setEnabled(false);
                btn_prod_novo.setEnabled(true);
                btn_prod_editar3.setEnabled(false);
                btn_prod_excluir3.setEnabled(true);
                break;
            
            case "Novo":
                btn_prod_salvar.setEnabled(true);
                btn_prod_voltar.setEnabled(true);
                c_prod_qtd.setEnabled(true);
                c_prod_nome.setEnabled(true);
                btn_prod_novo.setEnabled(false);
                btn_prod_editar3.setEnabled(false);
                btn_prod_excluir3.setEnabled(false);
                break;
                
            case "Editar":
                btn_prod_salvar.setEnabled(true);
                btn_prod_voltar.setEnabled(true);
                c_prod_qtd.setEnabled(true);
                c_prod_nome.setEnabled(true);
                btn_prod_novo.setEnabled(true);
                btn_prod_editar3.setEnabled(false);
                btn_prod_excluir3.setEnabled(false);
                break;
                
            case "Excluir":
                btn_prod_salvar.setEnabled(false);
                btn_prod_voltar.setEnabled(false);
                c_prod_qtd.setEnabled(false);
                c_prod_nome.setEnabled(false);
                btn_prod_novo.setEnabled(true);
                btn_prod_editar3.setEnabled(false);
                btn_prod_excluir3.setEnabled(false);
                break;
                
            case "Selecao":
                btn_prod_salvar.setEnabled(false);
                btn_prod_voltar.setEnabled(false);
                c_prod_qtd.setEnabled(false);
                c_prod_nome.setEnabled(false);
                btn_prod_novo.setEnabled(true);
                btn_prod_editar3.setEnabled(true);
                btn_prod_excluir3.setEnabled(true);
                break;
            default: System.out.println("Modo inv√°lido");
        }
    }
