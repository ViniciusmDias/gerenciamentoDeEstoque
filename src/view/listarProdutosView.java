    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tbl_prod_prods.getModel();
        modelo.setNumRows(0);
        ProdutoDao pdao = new ProdutoDao();
        
        for(Produto p: pdao.read()){
            
            modelo.addRow(new Object[]{
                
                p.getQuantidade(),
                p.getNome()
                
                
            });
            
        }
        
    }
    
    public void ManipulaInterfaceProd(){
        switch(modoProd){
            case "Navegar":
                btn_prod_editar.setEnabled(false);
                btn_prod_excluir.setEnabled(false);
                break;
            
            case "Novo":
               
                btn_prod_editar.setEnabled(false);
                btn_prod_excluir.setEnabled(false);
                break;
                
            case "Editar":
                btn_prod_editar.setEnabled(false);
                btn_prod_excluir.setEnabled(false);
                break;
                
            case "Excluir":
                btn_prod_editar.setEnabled(false);
                btn_prod_excluir.setEnabled(false);
                break;
                
            case "Selecao":
                btn_prod_editar.setEnabled(true);
                btn_prod_excluir.setEnabled(true);
                break;
            default: System.out.println("Modo inv√°lido");
        }
    }
